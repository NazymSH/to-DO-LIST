export default function OnePage() {
  const [selectedYear, setSelectedYear] = useState("");

  // Қол жетімді жылдар тізімі (year-ларды сорттаймыз)
  const years = timelineData.map((item) => item.year).sort((a, b) => a - b);

  // Таңдалған жылға сәйкес оқиғалар
  const selectedEvents = timelineData.find(
    (item) => item.year.toString() === selectedYear
  );

  return (
    <div className="containerA">
      <h1 className="title">Интерактивті Уақыт Сызығы</h1>

      <div className="selector-wrapper">
        <label htmlFor="yearSelect" className="label">
          Жыл таңдаңыз:
        </label>
        <select
          id="yearSelect"
          value={selectedYear}
          onChange={(e) => setSelectedYear(e.target.value)}
          className="year-select"
        >
          <option value="">-- Жылды таңдаңыз --</option>
          {years.map((year) => (
            <option key={year} value={year}>
              {year}
            </option>
          ))}
        </select>
      </div>

      {selectedEvents ? (
        <div className="events-wrapper">
          <h2 className="year-title">{selectedEvents.year} жылғы оқиғалар</h2>
          {selectedEvents.events.map((evt, idx) => (
            <div key={idx} className="event-card">
              <h3 className="event-person">{evt.person}</h3>
              <p className="event-desc">
                <strong>Оқиға:</strong> {evt.event}
              </p>
              <p className="event-desc">{evt.description}</p>
              {evt.media && (
                <img
                  src={evt.media}
                  alt={evt.person}
                  className="event-media"
                  loading="lazy"
                />
              )}
            </div>
          ))}
        </div>
      ) : (
        <p className="no-selection">Жылды таңдаңыз, оқиғалар осында шығады.</p>
      )}
    </div>
  );
}
