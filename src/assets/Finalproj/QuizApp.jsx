function QuizApp() {
  const [selected, setSelected] = useState(""); 
  const [qIndex, setQIndex] = useState(0); 
  const [score, setScore] = useState(0); 
  const [finished, setFinished] = useState(false); 
 
  const startQuiz = (name) => {
    setSelected(name);
    setQIndex(0);
    setScore(0);
    setFinished(false);
  };

  const answer = (option) => {
    if (option === quizData[selected][qIndex].answer) {
      setScore(score + 1);
    }

    if (qIndex + 1 < quizData[selected].length) {
      setQIndex(qIndex + 1);
    } else {
      setFinished(true);
    }
  };

  const reset = () => {
    setSelected("");
    setQIndex(0);
    setScore(0);
    setFinished(false);
  };

  if (!selected) {
    return (

      <div className="quiz-container">

< Link to="/AnswerPage" >
             <img src="https://cdn-icons-png.freepik.com/512/3114/3114883.png" alt="" width={40} height={40} />
            </Link>
        <h2>Тұлғаны таңдаңыз:</h2>
        {Object.keys(quizData).map((name,index) => (
          <button 
          key={name} 
          onClick={() => startQuiz(name)} 
        className={`figure-button ${index % 2 === 0 ? "left":"right"}`}
            >
            {name}
          </button>
        ))}
      </div>
    );
  }

  if (finished) {
    return (
      <div className="quiz-containerf">
        <h2>Нәтиже:</h2>
        <p>
          {selected} туралы {score}/{quizData[selected].length} дұрыс жауап бердіңіз.
        </p>
        <button onClick={reset} className="restart-button">Қайта бастау</button>
      </div>
    );
  }

  const current = quizData[selected][qIndex];

  return (
    <> 
            
    <div className="quiz-containers">
    < Link to="/Chatpage" >
             <img src="https://cdn-icons-png.freepik.com/512/3114/3114883.png" alt="" width={40} height={40} />
            </Link>
      <h3>{current.question}  </h3> 
      
      
      {current.options.map((opt, i) => (
        <button key={i} onClick={() => answer(opt)} className="answer-button">
          {opt}
        </button>
       
      ))}
      {/* <p className="guizanswer">
        Сұрақ {qIndex + 1} / {quizData[selected].length}
      </p> */}
    </div>
  </>
  );
}
 
export default QuizApp;
